package com.xiit.dao;

public class user {
    private String str;

    public String getStr() {
        return str;
    }

    public void setStr(String str) {
        this.str = str;
    }

    @Override
    public String toString() {
        return "user{" +
                "str='" + str + '\'' +
                '}';
    }
}
